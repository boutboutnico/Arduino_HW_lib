#include "Arduino.h"

#include "streaming.h"

#include "srf05.h"

/// ------------------------------------------------------------------------------------------------
/// NAMESPACE
/// ------------------------------------------------------------------------------------------------
using namespace arduino_hw_lib;

/// ------------------------------------------------------------------------------------------------
/// GLOBALE VARIABLES
/// ------------------------------------------------------------------------------------------------
SRF05 sfr05(11, 10);

/// ------------------------------------------------------------------------------------------------
/// SETUP
/// ------------------------------------------------------------------------------------------------

void setup(void)
{
	Serial.begin(9600);
}

/// ------------------------------------------------------------------------------------------------
/// LOOP
/// ------------------------------------------------------------------------------------------------

void loop(void)
{
	static const uint16_t ui16_delay = 300;

	Serial << "--------------------" << endl;
	Serial << "Distance: \t" << sfr05.getDistance_cm() << endl;

	delay(ui16_delay);
}

